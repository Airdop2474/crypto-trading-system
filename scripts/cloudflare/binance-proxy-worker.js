/**
 * Binance API Reverse Proxy - Cloudflare Worker
 *
 * Purpose: US IPs get HTTP 451 from Binance. This Worker proxies requests
 * through Cloudflare edge nodes (non-US egress) to bypass geo-restriction.
 *
 * Deploy guide: docs/BINANCE_PROXY_SETUP.md
 *
 * Traffic types:
 * 1. REST API (ccxt overrides urls["api"])
 *    - /testnet/* -> testnet.binance.vision/* (testnet, with API Key)
 *    - /main/*    -> api.binance.com/*        (mainnet, public/private)
 * 2. WebSocket live ticker
 *    - /main/ws/* -> wss://stream.binance.com:9443/ws/* (WebSocketPair bridge)
 *
 * Note: ccxt set_sandbox_mode(true) switches urls["api"] to testnet host.
 * We then override with Worker URL + original path, so Worker just passes through.
 */

const UPSTREAM_MAP = {
  "testnet.binance.vision": "testnet.binance.vision",
  "api.binance.com": "api.binance.com",
  "testnet.binancefuture.com": "testnet.binancefuture.com",
  // WebSocket upstream
  "stream.binance.com": "stream.binance.com",
};

// CORS headers applied to all REST responses
const CORS_HEADERS = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Methods": "GET, POST, PUT, DELETE, OPTIONS",
  "Access-Control-Allow-Headers": "Content-Type, X-MBX-APIKEY",
  "Access-Control-Max-Age": "86400",
};

/**
 * Strip CF-injected and hop-by-hop headers before forwarding upstream.
 * Keeps business headers like Content-Type and X-MBX-APIKEY.
 */
function cleanHeaders(originalHeaders, upstreamHost) {
  const h = new Headers();
  const BLOCKLIST = [
    // Cloudflare injected headers
    "cf-connecting-ip", "cf-ipcountry", "cf-ray", "cf-visitor",
    "cf-request-id", "cf-ew-via", "cdn-loop",
    // hop-by-hop headers
    "host", "connection", "upgrade",
    "sec-websocket-key", "sec-websocket-version",
    "sec-websocket-extensions", "sec-websocket-accept",
  ];
  for (const [key, value] of originalHeaders) {
    if (!BLOCKLIST.includes(key.toLowerCase())) {
      h.set(key, value);
    }
  }
  h.set("Host", upstreamHost);
  return h;
}

/**
 * WebSocket proxy using WebSocketPair API.
 * Flow: accept client WS -> fetch upstream WS -> bidirectional pipe.
 */
async function handleWebSocket(request, wsSubPath) {
  const upstreamUrl = `wss://stream.binance.com:9443/ws/${wsSubPath}`;
  const cleanH = cleanHeaders(request.headers, "stream.binance.com:9443");
  cleanH.set("Connection", "Upgrade");
  cleanH.set("Upgrade", "websocket");
  cleanH.set("Sec-WebSocket-Version", "13");
  // Sec-WebSocket-Key is auto-generated by fetch

  // 1. Connect to upstream WebSocket
  const upstreamResp = await fetch(upstreamUrl, {
    headers: cleanH,
  });

  const upstreamWs = upstreamResp.webSocket;
  if (!upstreamWs) {
    return new Response("WebSocket proxy: upstream did not upgrade", { status: 502 });
  }

  // 2. Create client WebSocket pair
  const pair = new WebSocketPair();
  const [client, server] = [pair[0], pair[1]];

  // 3. Accept client connection
  server.accept();

  // 4. Bidirectional pipe: client <-> upstream
  //    Silently catch errors (common in WS proxy disconnects)
  server.addEventListener("message", (event) => {
    try { upstreamWs.send(event.data); } catch (_) {}
  });
  server.addEventListener("close", () => {
    try { upstreamWs.close(); } catch (_) {}
  });
  server.addEventListener("error", () => {
    try { upstreamWs.close(); } catch (_) {}
  });

  upstreamWs.addEventListener("message", (event) => {
    try { server.send(event.data); } catch (_) {}
  });
  upstreamWs.addEventListener("close", () => {
    try { server.close(); } catch (_) {}
  });
  upstreamWs.addEventListener("error", () => {
    try { server.close(); } catch (_) {}
  });

  // 5. Return client WebSocket (triggers HTTP 101 Switching Protocols)
  return new Response(null, {
    status: 101,
    webSocket: client,
  });
}

export default {
  async fetch(request) {
    // CORS preflight
    if (request.method === "OPTIONS") {
      return new Response(null, { status: 204, headers: CORS_HEADERS });
    }

    const url = new URL(request.url);

    // Health check (no forwarding)
    if (url.pathname === "/" || url.pathname === "/health") {
      return new Response(JSON.stringify({ status: "ok", service: "binance-proxy" }), {
        headers: { "Content-Type": "application/json", ...CORS_HEADERS },
      });
    }

    // WebSocket upgrade: /main/ws/* -> wss://stream.binance.com:9443/ws/*
    const wsMatch = url.pathname.match(/^\/(testnet|main)\/ws\/(.*)$/);
    if (wsMatch && request.headers.get("upgrade") === "websocket") {
      return handleWebSocket(request, wsMatch[2]);
    }

    // REST API proxy
    let upstreamHost = request.headers.get("x-upstream-host") || "api.binance.com";

    // Route by path prefix: /testnet/* -> testnet.binance.vision, /main/* -> api.binance.com
    const pathParts = url.pathname.match(/^\/(testnet|main)(\/.*)?$/);
    if (pathParts) {
      upstreamHost = pathParts[1] === "testnet" ? "testnet.binance.vision" : "api.binance.com";
      url.pathname = pathParts[2] || "/";
    }

    if (!UPSTREAM_MAP[upstreamHost]) {
      return new Response(JSON.stringify({
        error: "unknown upstream",
        host: upstreamHost,
      }), { status: 400, headers: { "Content-Type": "application/json", ...CORS_HEADERS } });
    }

    // Build upstream request (strip CF headers, keep X-MBX-APIKEY)
    const upstreamUrl = `https://${upstreamHost}${url.pathname}${url.search}`;
    const headers = cleanHeaders(request.headers, upstreamHost);

    const upstreamReq = new Request(upstreamUrl, {
      method: request.method,
      headers: headers,
      body: request.method !== "GET" && request.method !== "HEAD" ? request.body : undefined,
      redirect: "manual",
    });

    try {
      const resp = await fetch(upstreamReq);
      const respHeaders = new Headers(resp.headers);
      // Append CORS headers
      for (const [k, v] of Object.entries(CORS_HEADERS)) {
        respHeaders.set(k, v);
      }
      return new Response(resp.body, {
        status: resp.status,
        statusText: resp.statusText,
        headers: respHeaders,
      });
    } catch (e) {
      return new Response(JSON.stringify({
        error: "upstream_fetch_failed",
        detail: String(e),
      }), { status: 502, headers: { "Content-Type": "application/json", ...CORS_HEADERS } });
    }
  },
};
